AS
SELECT        dbo.fin_Accounts.accountTitle, dbo.fin_Bills.*, dbo.fin_Generic_BillStatus.billStatusTitle, dbo.userProfile.userFirstName, dbo.userProfile.userLastName
FROM            dbo.fin_Bills INNER JOIN
                         dbo.fin_Accounts ON dbo.fin_Bills.billToEntityId = dbo.fin_Accounts.accountID INNER JOIN
                         dbo.fin_Generic_BillStatus ON dbo.fin_Bills.billStatus = dbo.fin_Generic_BillStatus.billStatusId INNER JOIN
                         dbo.userProfile ON dbo.fin_Bills.billCreatedBy = dbo.userProfile.userID
